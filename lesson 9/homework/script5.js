// simple calculator
function theFunction(number1,number2,operator){    
    if (operator == "+"){
        alert(number1 + number2)
    }else if (operator == "-"){
        alert(number1 - number2)
    }else if (operator == "*"){
        alert(number1 * number2)
    }else if (operator == "/"){
        alert(number1 / number2)
    }

}

theFunction(10,5,"*")

